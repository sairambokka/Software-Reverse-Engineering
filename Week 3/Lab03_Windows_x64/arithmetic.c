
int add(int a, int b) {
  return a + b;
}

int sub(int a, int b) {
  return a - b;
}

int mul(int a, int b) {
  return a * b;
}

int div(int a, int b) {
  return a / b;
}

int usub(unsigned int a, unsigned int b) {
  return a - b;
}

int umul(unsigned int a, unsigned int b) {
  return a * b;
}

int uadd(unsigned int a, unsigned int b) {
  return a + b;
}

int udiv(unsigned int a, unsigned int b) {
  return a / b;
}
